// Generate components/BrandIcon.tsx with real brand logos from simple-icons (CC0).
// Real marks, baked in as literal SVG paths — no runtime fetch, no emoji.
import { writeFile } from "node:fs/promises";

const ICONS = [
  { slug: "line", key: "line", title: "LINE", hex: "#06C755" },
  { slug: "facebook", key: "facebook", title: "Facebook", hex: "#0866FF" },
  { slug: "x", key: "x", title: "X", hex: "#000000" },
  { slug: "whatsapp", key: "whatsapp", title: "WhatsApp", hex: "#25D366" },
];

const out = {};
for (const ic of ICONS) {
  const res = await fetch(`https://raw.githubusercontent.com/simple-icons/simple-icons/develop/icons/${ic.slug}.svg`);
  if (!res.ok) throw new Error(`fetch ${ic.slug} → ${res.status}`);
  const svg = await res.text();
  const m = svg.match(/ d="([^"]+)"/);
  if (!m) throw new Error(`no path for ${ic.slug}`);
  out[ic.key] = { title: ic.title, hex: ic.hex, path: m[1] };
}

const file = `// AUTO-GENERATED by scripts/gen-brand-icons.mjs — real brand logos (simple-icons, CC0).
// Inline literal paths: no runtime fetch, no emoji. Regenerate to refresh.

export type BrandKey = ${ICONS.map((i) => `"${i.key}"`).join(" | ")};

export const BRANDS: Record<BrandKey, { title: string; hex: string; path: string }> = ${JSON.stringify(out, null, 2)};

export function BrandIcon({ brand, size = 18, color }: { brand: BrandKey; size?: number; color?: string }) {
  const b = BRANDS[brand];
  return (
    <svg role="img" viewBox="0 0 24 24" width={size} height={size} fill={color ?? "currentColor"} aria-hidden focusable="false">
      <path d={b.path} />
    </svg>
  );
}
`;
await writeFile("components/BrandIcon.tsx", file);
console.log(`wrote components/BrandIcon.tsx with ${Object.keys(out).length} brands`);
